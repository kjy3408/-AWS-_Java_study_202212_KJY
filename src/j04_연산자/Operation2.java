package j04_연산자;

public class Operation2 {
	public static void main(String[] args) {
		int num=10;
	
		num = num + 1;
	
		System.out.println(num++);
		//증감연산자가 앞에 붙으면 값을 올리고 출력(선증가)
		//증감연산자가 뒤에 붙으면 출력 후 값을 올림(후증가) => 더 많이 씀
		//후증가 쓰는 이유 : 1부터 시작이 아니라 0부터 시작일때. (-1하고 선증가 써도되긴하는데 비용낭비임)
		
		
		
		//1++ num (10) => 11
		//num (10) => 10 ++1
		
	
		
	}
}
