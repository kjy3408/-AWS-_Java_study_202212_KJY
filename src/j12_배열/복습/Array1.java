package j12_배열.복습;



public class Array1 {

	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 20;
		int num3 = 30;
		int num4 = 40;
		int num5 = 50;
		
		int[] nums = new int [5];
		nums[0] = num1;//10
		nums[1] = num2;//20
		nums[2] = num3;//30
		nums[3] = num4;//40
		nums[4] = num5;//50
	
		System.out.println(new int[] {1, 2, 3, 4, 5});		//이것자체로 배열임.
		System.out.println((new int[] {1, 2, 3, 4, 5})[0]);	//배열의 해당 인덱스 뽑아낼 수 있음. (위와는 다른 배열)

		
		
		
		
		
		
	}
}
